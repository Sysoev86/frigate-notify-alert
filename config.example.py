# ─────────────────────────────────────────────────────────────────────────
#  2. ГРУППЫ КАМЕР — что и куда слать
# ─────────────────────────────────────────────────────────────────────────
#  Группа = набор камер + один чат, куда по ним приходят уведомления.
#  Групп может быть одна или несколько. Например, камеры со двора шлём в один
#  чат, а с входа — в другой. Если тебе хватает одной группы — оставь только
#  "group1", а блок "group2" удали (вместе с запятой перед ним).
#
#  Для каждой группы задаётся:
#    telegram_chat_id — КУДА слать (см. ниже, как узнать ID чата)
#    cameras          — СПИСОК камер, имена РОВНО как в твоём Frigate
#    zones            — НЕОБЯЗАТЕЛЬНО. Список зон Frigate. Если задан — уведомление
#                       придёт, только когда объект зашёл в одну из этих зон
#                       (удобно, чтобы не реагировать на прохожих мимо). Если
#                       ключа нет или список пустой — шлём по всей камере.
#                       Имена зон бери из Frigate: config.yml → cameras.<камера>.zones.
#    mute_controls    — НЕОБЯЗАТЕЛЬНО. True (по умолч.) = в чате появятся кнопки
#                       «Пауза 15м/1ч/3ч/до утра», чтобы временно заглушить
#                       уведомления (например, пока гуляешь). False = без кнопок.
#                       Работает, если запущен сервис mute_controller (см. README).
#    name             — любое название для себя, попадает только в логи
#
#  Как узнать telegram_chat_id:
#    - Личка: напиши боту @userinfobot — он покажет твой numeric id.
#    - Группа/канал: добавь туда своего бота, затем напиши в чат @getidsbot
#      (или @username_to_id_bot). ID группы обычно начинается с -100...
#      Важно: бот должен быть УЧАСТНИКОМ чата, иначе не сможет туда писать.
#
#  Как узнать имена камер (cameras):
#    Это имена из конфига Frigate (config.yml), раздел `cameras:`.
#    Пиши их точь-в-точь (регистр важен). Пример из Frigate:
#        cameras:
#          dvor:        <- вот это имя, его и вписывай: "dvor"
#          vhod:
GROUPS = {
    "group1": {
        "telegram_chat_id": "ВСТАВЬ_ID_ЧАТА_1",   # чат для первой группы
        "cameras": ["kamera_vhod"],                # ← замени на свои имена камер
        # "zones": ["zone_vhod"],                  # ← раскомментируй, чтобы слать только по зоне
        "mute_controls": True,                     # кнопки паузы уведомлений в чате
        "name": "Группа 1 (вход)"
    },
    "group2": {
        "telegram_chat_id": "ВСТАВЬ_ID_ЧАТА_2",   # чат для второй группы
        "cameras": [                               # ← несколько камер — через запятую
            "kamera_dvor",
            "kamera_vorota",
            "kamera_parkovka"
        ],
        # "zones": ["zone_dvor", "zone_vorota"],   # ← необязательно; нет ключа = вся камера
        "mute_controls": True,                     # кнопки паузы уведомлений в чате
        "name": "Группа 2 (двор)"
    }
}


